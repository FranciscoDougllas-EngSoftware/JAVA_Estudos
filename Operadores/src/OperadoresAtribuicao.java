public class OperadoresAtribuicao {
    public static void main(String[] args) {
        //Em java temos vários operadores de atribuição
        // = atribuição simples
        // += atribuição com adição
        // -= atribuição com subtração
        // *= atribuição com multiplicação
        // /= atribuição com divisão
        // %= atribuição com resto da divisão
        // &= atribuição com AND
        // |= atribuição com OR
        // ^= atribuição com XOR
        // <<= atribuição com deslocamento à esquerda
        // >>= atribuição com deslocamento à direita
        // >>>= atribuição com deslocamento à direita sem sinal

        //Exemplo de uso do operador de atribuição simples
        int a = 10;
        int b = 20;
        int c = 30;
        int d = 40;
        System.out.println(a + b);
        //Exemplo de uso do operador de atribuição com adição
        a += b; // a = a + b
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com subtração
        a -= b; // a = a - b
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com multiplicação
        a *= b; // a = a * b
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com divisão
        a /= b; // a = a / b
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com resto da divisão
        a %= b; // a = a % b
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com AND
        a &= b; // a = a & b
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com OR
        a |= b; // a = a | b
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com XOR
        a ^= b; // a = a ^ b
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com deslocamento à esquerda
        a <<= 2; // a = a << 2
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com deslocamento à direita
        a >>= 2; // a = a >> 2
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com deslocamento à direita sem sinal
        a >>>= 2; // a = a >>> 2
        System.out.println(a);
        //Exemplo de uso do operador de atribuição com deslocamento à direita sem sinal
        a >>>= 2; // a = a >>> 2
        System.out.println(a);
    }
}
